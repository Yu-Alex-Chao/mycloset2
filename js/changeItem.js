function openInstrucAbove() {
  var insButName = document.getElementById("instruc-but-name");
  var insAbo = document.getElementById("instruc-above");
  // need to use "===" instead of "="
  // see: https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Statements/var
  if (insAbo.style.display === "none") {
    insAbo.style.display = "block";
    insButName.innerHTML = "关闭任务指导";
  } else {
    insAbo.style.display = "none";
    insButName.innerHTML = "打开任务指导";
  }
}


