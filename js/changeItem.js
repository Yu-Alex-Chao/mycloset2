function changeToItemA() {
  // change picture sources
  document.getElementById("clothpic").src = "images\\itemA1.jpg";
  document.getElementById("clothpic-md-1").src = "images\\itemA1.jpg";
  document.getElementById("clothpic-md-2").src = "images\\itemA2.jpg";

  // change name, cost and sold pieces of given item
  document.getElementById("name").innerHTML = goodname["itemA"].name;
  document.getElementById("cost").innerHTML = goodname["itemA"].price;
  document.getElementById("nsold").innerHTML = goodname["itemA"].nsold;
  document.getElementById("ncom").innerHTML = goodname["itemA"].ncom;

  // change description of the item (4 lines)
  var myDescrp = ["dscrp1","dscrp2","dscrp3","dscrp4"]
  var myLine = ["line1","line2","line3","line4"]
  for (var i = 0; i < 4; i ++) {
    document.getElementById(myDescrp[i]).innerHTML = goodname["itemA"].des[myLine[i]]
  }

  // making sure buttons are in their originial css when changed items
  var myTopArray = ['a','b','c','d','e','f']
  var arrayLength = myTopArray.length;
  for (var i = 0; i < arrayLength; i++) {
    document.getElementById(myTopArray[i]).style = "background-color: #fff !important";
  }

  // change colors according to each item
  var colLength = goodname["itemA"].col.length
  var myButtomArray = ['A','B','C','D','E','F']
  for(i = 0; i < colLength; i++) {
    document.getElementById(myButtomArray[i]).style.display = 'inline';
    document.getElementById(myButtomArray[i]).innerHTML = goodname["itemA"].col[i];
    document.getElementById(myButtomArray[i]).style = "background-color: #fff !important";
  }
  for(i = colLength; i < 6; i++) {
    document.getElementById(myButtomArray[i]).style.display = 'none';
  }

  document.getElementById("info1").innerHTML = goodname["itemA"].info["info1"];
  document.getElementById("info2").innerHTML = goodname["itemA"].info["info2"];

};



